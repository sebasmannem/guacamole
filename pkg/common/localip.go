package common

import (
	"log"
	"net"
)

// Get preferred outbound ip of this machine
// This is not actually creating a connection, just faking it to get the route
// See https://stackoverflow.com/questions/23558425/how-do-i-get-the-local-ip-address-in-go for more info
func GetOutboundIP() net.IP {
	conn, err := net.Dial("udp", "8.8.8.8:80")
	if err != nil {
		log.Fatal(err)
	}
	defer conn.Close()

	localAddr := conn.LocalAddr().(*net.UDPAddr)

	return localAddr.IP
}
